package com.Type;

public class Retprorec {
    public int chapter_num;
    public int chapter_rec;

    public Retprorec(int chapter_num, int chapter_rec) {
        chapter_num = chapter_num;
        chapter_rec = chapter_rec;
    }

    public int getChapter_num() {
        return chapter_num;
    }

    public void setChapter_num(int chapter_num) {
        chapter_num = chapter_num;
    }

    public int getChapter_rec() {
        return chapter_rec;
    }

    public void setChapter_rec(int chapter_rec) {
        chapter_rec = chapter_rec;
    }
}
